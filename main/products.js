// Функция для поиска товара по id и категории
function findProduct(category, id) {
    if (products[category]) {
        return products[category].find(p => p.id === id);
    }
    return null;
}

// Функция для получения всех товаров категории
function getProductsByCategory(category) {
    return products[category] || [];
}

// Функция для получения всех товаров
function getAllProducts() {
    return {
        clothing: products.clothing,
        footwear: products.footwear,
        accessories: products.accessories
    };
}
